package bss;

public class Bss {
	private int bssID;
	private String bssTitle;
	private String userID;
	private String bssDate;
	private String bssContent;
	private int bssAvailable;
	private int readcnt;
	
	public int getBssID() {
		return bssID;
	}
	public void setBssID(int bssID) {
		this.bssID = bssID;
	}
	public String getBssTitle() {
		return bssTitle;
	}
	public void setBssTitle(String bssTitle) {
		this.bssTitle = bssTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getBssDate() {
		return bssDate;
	}
	public void setBssDate(String bssDate) {
		this.bssDate = bssDate;
	}
	public String getBssContent() {
		return bssContent;
	}
	public void setBssContent(String bssContent) {
		this.bssContent = bssContent;
	}
	public int getBssAvailable() {
		return bssAvailable;
	}
	public void setBssAvailable(int bssAvailable) {
		this.bssAvailable = bssAvailable;
	}
	public int getReadcnt() {
		return readcnt;
	}
	public void setReadcnt(int readcnt) {
		this.readcnt = readcnt;
	}
	
	
	

}
